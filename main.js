// Build a basic arithmetic calculator without a frontend
// The calculator should be able to perform basic operations like Addition, Subtraction, 
// Multiplication, & Division

const add = (a, b) => {
  return a + b;
}

const subtract = (a, b) => {
  return a - b;
}

const multiply = (a, b) => {
  return a * b;
}

const divide = (a, b) => {
  return a / b;
}
